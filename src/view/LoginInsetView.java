   private boolean validarCamposObrigatorios() {
        if (jtfSenha.getText().equals("") & jtfUsuario.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Senha e Usuario e de preenchimento obrigatÃ³rio");
            jtfSenha.requestFocus();
            return false;
        }

        return true;
    }

    public Login getDados() {
        Login c = new Login();

      
        
        c.setUsuario(jtfUsuario.getText());
        c.setSenha(jtfSenha.getText());
        c.setAdm(jcbAdm.isSelected());

        return c;
    }

    public void setDados(Login l) {
        
        jtfId.setText(l.getId().toString());
        jtfUsuario.setText(l.getUsuario());
        jtfSenha.setText(l.getSenha());
        jcbAdm.setSelected(l.getAdm());

    }
