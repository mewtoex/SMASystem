    private void jBConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBConsultarActionPerformed

        List<Assistidos> c = AssistidosController.getInstance().
                busca(jcB.getSelectedIndex(),
                        jCOpcoes.getText());

        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("Id");
        modelo.addColumn("Nome");
        modelo.addColumn("RG");
        modelo.addColumn("Telefone");
        modelo.addColumn("Profiss√£o");
        modelo.addColumn("Cpf");
        modelo.addColumn("Renda");
        modelo.addColumn("Idade");
        modelo.addColumn("Rua");
        modelo.addColumn("Numero");
        modelo.addColumn("Complemento");
        modelo.addColumn("Bairro");
        modelo.addColumn("Cidade");
        modelo.addColumn("Estado");
        modelo.addColumn("Ativo");

        for (Assistidos i : c) {
            // Seta os valores do objeto para o JTableModel 
            modelo.addRow(new Object[]{i.getId(), i.getNome(), i.getIdentidade(), i.getTelefone(), i.getProfissao(), i.getCPF(), i.getRenda(), i.getIdade(), i.getRua(), i.getNumero(), i.getComplemento(), i.getBairro(), i.getCidade(), i.getEstado(), i.getAtivo()});
        }
        //Limpa a JTable (Grid)
        jTAssistido.removeAll();
        //Seta o modelo para a Grid 
        jTAssistido.setModel(modelo);

        //Ajusta o tamanho da primeira coluna 
        jTAssistido.getColumnModel().getColumn(0).setPreferredWidth(60);
        jTAssistido.getColumnModel().getColumn(0).setPreferredWidth(360);
        jTAssistido.getColumnModel().getColumn(0).setPreferredWidth(60);
        jTAssistido.getColumnModel().getColumn(0).setPreferredWidth(50);
        jTAssistido.getColumnModel().getColumn(0).setPreferredWidth(50);
        jTAssistido.getColumnModel().getColumn(0).setPreferredWidth(50);
        jTAssistido.getColumnModel().getColumn(0).setPreferredWidth(60);
        jTAssistido.getColumnModel().getColumn(0).setPreferredWidth(60);
        jTAssistido.getColumnModel().getColumn(0).setPreferredWidth(60);
        jTAssistido.getColumnModel().getColumn(0).setPreferredWidth(60);
        jTAssistido.getColumnModel().getColumn(0).setPreferredWidth(60);
        jTAssistido.getColumnModel().getColumn(0).setPreferredWidth(60);
        jTAssistido.getColumnModel().getColumn(0).setPreferredWidth(60);
    }//GEN-LAST:event_jBConsultarActionPerformed

