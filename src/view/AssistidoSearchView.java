    private void jBAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBAlterarActionPerformed
        int linha = jTAssistido.getSelectedRow();

        //Verifica se existe alguem selecionado 
        if (linha >= 0) {
            Long id = (Long) jTAssistido.getValueAt(linha, 0);

            Assistidos Assistido = AssistidosController.getInstance().buscarID(id);

            AssistidoEditView v = new AssistidoEditView();
            v.setLocationRelativeTo(null);
            v.setDados(Assistido);
            v.logo(false);
            v.setVisible(true);

            jBConsultar.doClick();
    }//GEN-LAST:event_jBAlterarActionPerformed
else {
            JOptionPane.showMessageDialog(this, "Nenhum Registro foi selecionado.. seu burro.");
        }

    }
