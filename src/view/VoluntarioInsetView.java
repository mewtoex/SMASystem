    private boolean validarCamposObrigatorios() {
        if (jtfIdentidade.getText().equals("") & jtfNome.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Nome e CPF e de preenchimento obrigatório");
            jtfCPF.requestFocus();

            return false;
        }
        if (!isCPF()) {
            JOptionPane.showMessageDialog(this, "CPF Invalido");
            return false;
        }
        
        else {
            return true;
        
        }
    }

